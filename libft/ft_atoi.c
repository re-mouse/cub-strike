#include "libft.h"

int	ft_atoi(const char *str)
{
	int sign;
	int ret;

	ret = 0;
	sign = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
		if (*str++ == '-')
			sign = -sign;
	while (*str >= '0' && *str <= '9')
		ret = ret * 10 + *str++ - '0';
	return (ret * sign);
}
